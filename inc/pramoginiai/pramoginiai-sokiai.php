O kur dar salsos, bačiatos bei svingo pagrindai, kurie jau yra tapę savaime suprantami bet kuriame šokių 
vakarėlyje.. Skamba neįtikėtinai?

Jokios paslapties nėra, - kai susiduriate su išmania šokių studija ir mokytojais, 
kurie mėgsta iššūkius, tada daug kas tampa įmanoma. Pamokose mes remiamės 10 metų patirtimi ir 
naudojame ne tik šokio, bet ir pedagogikos, psichologijos bei kitas žinias, nuolat jomis keičiamės ir atnaujiname. 
Juk svarbiausia, kad Jūsų įdėtos pastangos atsipirktų su dideliu malonumo kaupu!

