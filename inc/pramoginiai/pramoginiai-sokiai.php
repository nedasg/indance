Vienus į šokių pamokas atveda neišvengiama būtinybė: viena kita išvyka į užsienio šalis, 
vakarėliai po dalykinių susitikimų, konferencijų ir... kelios nejaukios akimirkos, kai į kvietimą 
šokti tenka atsakyti "ne". Kita priežastis - artėjančios išleistuvės ar vestuvės, kur valsas - 
tradicinė iškilmių dalis. Dar viena grupė žmonių, o jų dauguma, - tai ieškantys vietos, kur 
būtų galima smagiai leisti laiką kartu su gyvenimo partneriu ar grupe draugų, bendradarbių. 
Paprastai ieškoma tokios poilsio formos, kur nereiktų lieti prakaito, bet kūnas visgi gautų tam 
tikrą fizinį krūvį, kur galima būtų išmokti ko nors naujo, bet nesudėtingo ir nereikalaujančio 
didelių pastangų, ir, galų gale, kas teiktų džiaugsmą, atgaivintų, leistų užmiršti kasdienius darbus ir rūpesčius.
<br>
Pramoginių šokių sąvoka talpina daug įvairių, net ir labai skirtingų, šokių, kuriuos jungia tai, kad šie šokiai 
gali būti šokami ne tik pasirodymui, ne tik publikai, o savo malonumui, pramogai. Čia svarbiausia - teigiamos 
emocijos, poilsis, malonus bendravimas poroje ir šokančiųjų grupėje. Kad lankytojai išmoktų sėkmingai šokti 
pramoginius šokius, nereikia ypatingų gebėjimų, išankstinio specialaus pasiruošimo, idealios klausos ar tobulo 
ritmo pojūčio. Pramoginiame šokyje naudojami paprasčiausi šokių judesiai, kuriuos gali greitai išmokti kiekvienas.
